create or ALTER  view SIMIX_ITEM_PRINCIPAL (
    CODIGO_INTERNO,
    CODIGO_BARRAS,
    DESCRICAO,
    DEPARTAMENTO,
    CATEGORIA,
    DATAALTERACAO,
    FOTO)
as
select P.PRODICOD, P.PRODA60CODBAR, P.PRODA60DESCR, G.GRUPA60DESCR, SG.SUBGA60DESCR, P.PRODDULTALTER, ''
from PRODUTO P
left join GRUPO G on P.GRUPICOD = G.GRUPICOD
left join SUBGRUPO SG on SG.GRUPICOD = G.GRUPICOD and P.SUBGICOD = SG.SUBGICOD ;